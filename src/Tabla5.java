//Desarrollar un programa que muestre la tabla de multiplicar del 5 (del 5 al 50)
public class Tabla5 {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            int resultado = 5 * i;
            System.out.println("5 x " + i + " = " + resultado);
        }
    }
}
