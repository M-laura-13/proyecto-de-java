public class BucleFor {
    public static void main(String[] args) {
        // Scanner scanner = new scanner(System.in);
        int[] numeros= {1, 2, 3, 4};
        for (int i = 0; i < numeros.length; i++) {
            System.out.println("Número en índice " + i + ": " + numeros[i]);
        }
    }
    
}
